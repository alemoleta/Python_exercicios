# dados de exemplo para variáveis a converter
real = 10 
dolar = 2 

# cálculos das conversões na cotação de 20 de setembro de 2021
dolar_to_real = dolar * 5.29
real_to_dolar = real / 5.29

# apresentação dos resultados arredondados
print(dolar, "dolars is", round(dolar_to_real, 2), "reals")
print(real, "reals is", round(real_to_dolar, 2), "dolars")
